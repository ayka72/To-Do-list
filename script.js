function addTask_mon(){
    const val = document.querySelector(".input-in-mon").value;
    if (val === ""){
      alert("Try write sth")
    }
    else{
      const task = document.querySelector("#mon-task");
      const li = document.createElement("li");
      li.innerHTML = val;
      task.appendChild(li)
    }
    document.querySelector(".input-in-mon").value = "";
}

